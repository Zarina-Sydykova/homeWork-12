public class School {
    private String student;
    private int age;
    private String group;

    public void setStudent(String student){
        this.student=student;
    }
    public void setAge(int age){
        this.age=age;
    }
    public void setGroup(String group){
        this.group=group;
    }
    String getStudent(){
        return student;
    }
    int getAge(){
        return age;
    }
    String getGroup(){
        return group;
    }
}

public class University {
    private String name;
    private String address;
    private int year;
    public void setName(String name){
        this.name=name;
    }
    public void setAddress(String address){
        this.address=address;
    }
    public void setYear(int year){
        this.year=year;
    }
    String getName(){
        return name;
    }
    String getAddress(){
        return address;
    }
    int getYear(){
        return year;
    }


}
